package com.example.secondversion5pr.data.model;

public class River {
    private int image;
    private String nameLake;
    public River(int image, String nameLake) {
        this.image = image;
        this.nameLake = nameLake;
    }
    public int getImage() {
        return image;
    }
    public String getNameItem() {
        return nameLake;
    }
}
